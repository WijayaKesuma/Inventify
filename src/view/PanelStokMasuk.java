    private void btnEditStokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditStokActionPerformed
        if (selectedIdMasuk == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu data stok yang mau diedit!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String jumlahStr = txtJumlahStok.getText().trim();
        String hargaStr = txtHargaBeli.getText().trim();

        if (jumlahStr.isEmpty() || hargaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jumlah Stok dan Harga Beli wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int jumlah;
        double harga;
        try {
            jumlah = Integer.parseInt(jumlahStr);
            harga = Double.parseDouble(hargaStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah Stok dan Harga Beli harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (jumlah <= 0 || harga <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah Stok dan Harga Beli harus lebih dari 0!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean berhasil = stokMasukController.editStokMasuk(selectedIdMasuk, jumlah, harga);

        if (berhasil) {
            JOptionPane.showMessageDialog(this, "Data stok berhasil diupdate!");
            clearFormStokMasuk();
            loadTableStokMasuk();
        }
    }//GEN-LAST:event_btnEditStokActionPerformed

    private void tableStokMasukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableStokMasukMouseClicked
        int row = tableStokMasuk.getSelectedRow();
        if (row != -1 && listStokMasukSekarang != null) {
            StokMasuk sm = listStokMasukSekarang.get(row);
            selectedIdMasuk = sm.getIdMasuk();

            txtStokSupplier.setText(sm.getNamaSupplier());
            txtStokSepatu.setText(sm.getNamaSepatu());
            txtJumlahStok.setText(String.valueOf(sm.getJumlah()));
            txtHargaBeli.setText(String.valueOf(sm.getHargaBeli()));

            // Lock field supplier & sepatu saat mode edit (cuma jumlah & harga yang boleh dikoreksi)
            txtStokSupplier.setEditable(false);
            txtStokSepatu.setEditable(false);
        }
    }//GEN-LAST:event_tableStokMasukMouseClicked

    private void btnDeleteStokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteStokActionPerformed
        if (selectedIdMasuk == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu data stok yang mau dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Menghapus data ini akan mengurangi stok sepatu terkait. Yakin ingin melanjutkan?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            boolean berhasil = stokMasukController.deleteStokMasuk(selectedIdMasuk);
            if (berhasil) {
                JOptionPane.showMessageDialog(this, "Data stok berhasil dihapus, stok sepatu telah disesuaikan.");
                clearFormStokMasuk();
                loadTableStokMasuk();
            }
        }
    }//GEN-LAST:event_btnDeleteStokActionPerformed


