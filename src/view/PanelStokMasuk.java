    private void loadTableStokMasuk() {
        String[] judulKolom = {"Tanggal", "Nama Supplier", "Nama Sepatu", "Stok", "Harga/pcs", "Total Harga"};
        tableModelStokMasuk = new DefaultTableModel(judulKolom, 0);

        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy");

        listStokMasukSekarang = stokMasukController.getAllStokMasuk(); // simpan buat referensi klik row
        for (StokMasuk sm : listStokMasukSekarang) {
            Object[] rowData = {
                sdf.format(sm.getTanggal()),
                sm.getNamaSupplier(),
                sm.getNamaSepatu(),
                sm.getJumlah(),
                String.format("%,.0f", sm.getHargaBeli()),
                String.format("%,.0f", sm.getTotalHarga())
            };
            tableModelStokMasuk.addRow(rowData);
        }
        tableStokMasuk.setModel(tableModelStokMasuk);
        
        TableDecorator.decorateTableStok(tableStokMasuk);
    }
    
