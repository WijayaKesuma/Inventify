    private void btnTambahStokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahStokActionPerformed
        // Validasi supplier
        if (selectedSupplierIdForStok == -1) {
            JOptionPane.showMessageDialog(this, "Pilih supplier terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi sepatu
        if (selectedSepatuIdForStok.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih sepatu terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi jumlah & harga
        String jumlahStr = txtJumlahStok.getText().trim();
        String hargaStr = txtHargaBeli.getText().trim();

        if (jumlahStr.isEmpty() || hargaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jumlah stok dan harga beli wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int jumlah;
        double harga;
        try {
            jumlah = Integer.parseInt(jumlahStr);
            harga = Double.parseDouble(hargaStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah stok dan harga beli harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (jumlah <= 0 || harga <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah stok dan harga beli harus lebih dari 0!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double totalHarga = jumlah * harga;

        StokMasuk sm = new StokMasuk(selectedSupplierIdForStok, selectedSepatuIdForStok, jumlah, harga, totalHarga);
        boolean berhasil = stokMasukController.tambahStokMasuk(sm);

        if (berhasil) {
            JOptionPane.showMessageDialog(this, "Stok berhasil ditambahkan!");
            clearFormStokMasuk();
            loadTableStokMasuk();
        }
    
    }//GEN-LAST:event_btnTambahStokActionPerformed

